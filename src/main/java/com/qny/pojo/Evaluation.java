package com.qny.pojo;

import java.util.Date;

public class Evaluation {
	
	//评价表Id，唯一标识
	private Integer id;
	//物品编号
	private String itemNumber;
	//租客Id
	private String zukeId;
	//出租人Id
	private String chuzuId;
	//评价时间
	private Date time;
	//评价内容
	private String content;
	//评价的用户名
	private String userName;
	//评价的物品
	private Item item;
	//租客电话号码
	private String phoneNumber;

	
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getItemNumber() {
		return itemNumber;
	}
	public void setItemNumber(String itemNumber) {
		this.itemNumber = itemNumber;
	}
	public String getZukeId() {
		return zukeId;
	}
	public void setZukeId(String zukeId) {
		this.zukeId = zukeId;
	}
	public String getChuzuId() {
		return chuzuId;
	}
	public void setChuzuId(String chuzuId) {
		this.chuzuId = chuzuId;
	}

	public Date getTime() {
		return time;
	}
	public void setTime(Date time) {
		this.time = time;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public Item getItem() {
		return item;
	}
	public void setItem(Item item) {
		this.item = item;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}

	
	

}
